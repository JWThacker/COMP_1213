/**
   A simple program to print the letter "J" using the word "Java"
   as an output stream to the I/O window.
   @author Jared Thacker
   @version January 21, 2021
*/
public class JLetter {
/**
   This program prints the letter "J" using a series of print
   statements.
   @param args Command line argumnents - not used
   @returns: prints text to the screen
*/
   public static void main(String[] args) {
      System.out.println("JAVAJAVAJAVA");
      System.out.println("JAVAJAVAJAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("J     JAVA");
      System.out.println("JA    JAVA");
      System.out.println(" JAVAJAVA ");
      System.out.println("  JAVAJA  ");
   }
}